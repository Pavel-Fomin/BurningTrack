import Foundation

/// Описывает фазу жизненного цикла одной операции экспорта.
enum ExportState: String, Equatable, Sendable {
    /// Операция ещё не запускалась.
    case idle

    /// Сервис собирает доступные исходные файлы и их размеры.
    case preparing

    /// Сервис копирует файлы в выбранную папку.
    case copying

    /// Все доступные файлы скопированы без ошибок.
    case completed

    /// Копирование завершено, но один или несколько файлов не удалось обработать.
    case completedWithErrors

    /// Операция остановлена пользователем или отменена владельцем задачи.
    case cancelled

    /// Операция остановлена из-за ошибки инфраструктуры назначения.
    case failed
}
