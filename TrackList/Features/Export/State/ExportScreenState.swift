import Foundation

/// Содержит все данные, необходимые интерфейсу для отображения экспорта.
struct ExportScreenState: Equatable {

    /// Текущая фаза отображаемой операции.
    enum Phase: Equatable {
        /// Экспорт не отображается, потому что снимок прогресса отсутствует.
        case hidden

        /// Сервис подготавливает источники и место назначения.
        case preparing

        /// Сервис последовательно копирует файлы.
        case copying

        /// Все файлы завершены без индивидуальных ошибок.
        case completed

        /// Операция завершена, но часть файлов не обработана.
        case completedWithErrors

        /// Операция отменена пользователем или владельцем задачи.
        case cancelled

        /// Операция завершилась инфраструктурной ошибкой.
        case failed
    }

    /// Текущая фаза состояния экспорта.
    let phase: Phase

    /// Исходный снимок прогресса для существующих счётчиков и подробностей.
    let progress: ExportProgress?

    /// Показывает, должна ли отображаться компактная панель экспорта.
    let isVisible: Bool

    /// Показывает, выполняется ли выбор назначения или копирование.
    let isExportActive: Bool

    /// Показывает, доступна ли пользователю отмена операции.
    let canCancel: Bool

    /// Показывает, открыт ли подробный экран экспорта.
    let isShowingDetails: Bool
}
