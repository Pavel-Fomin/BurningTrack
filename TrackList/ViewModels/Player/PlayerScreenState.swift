import Foundation

/// Хранит готовое состояние высокоуровневого экрана плеера без выполнения действий.
struct PlayerScreenState {

    /// Состояния строк плейлиста в порядке их отображения.
    var rows: [PlayerTrackRowState]

    /// Идентификатор строки, к которой должен прокрутиться список.
    var scrollTargetId: UUID?

    /// Количество треков в плейлисте плеера.
    var trackCount: Int

    /// Определяет, доступно ли действие экспорта плейлиста.
    var canExport: Bool

    /// Определяет, доступно ли действие очистки плейлиста.
    var canClear: Bool
}

/// Хранит готовое состояние одной строки плейлиста плеера без выполнения действий.
struct PlayerTrackRowState: Identifiable {

    /// Идентификатор конкретного вхождения трека в очередь плеера.
    let id: UUID

    /// Идентификатор трека в фонотеке.
    let trackId: UUID

    /// Исходная модель трека плеера.
    let track: PlayerTrack

    /// Определяет, является ли трек текущим в контексте плеера.
    let isCurrent: Bool

    /// Определяет, воспроизводится ли текущий трек.
    let isPlaying: Bool

    /// Определяет, должна ли строка отображаться выделенной.
    let isHighlighted: Bool

    /// Лёгкий запрос обложки трека для общей асинхронной подсистемы.
    let artworkRequest: ArtworkRequest?

    /// Подготовленный заголовок строки.
    let title: String

    /// Подготовленное имя исполнителя.
    let artist: String

    /// Актуальное имя файла для сценария переименования.
    let currentFileName: String

    /// Исполнитель для сценария автоматического переименования.
    let renameArtist: String?

    /// Название трека для сценария автоматического переименования.
    let renameTitle: String?

    /// Подготовленная длительность трека в секундах.
    let duration: TimeInterval?

    /// Определяет, нужно ли отображать формат файла.
    let showsFileFormat: Bool
}
